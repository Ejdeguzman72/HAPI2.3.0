    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRow();
        Object index = jTable1.getValueAt(row, 0);

        System.out.println("this is object " + index);

        final String dbhost = "jdbc:postgresql://localhost:5432/InventoryTrackerDB";

        final String user = "postgres";

        final String password = "password";

        String delRow = "DELETE FROM inventory where inventory_id =" + index;

        try {

            if (jTable1.getSelectedRowCount() == 1) {
                Connection connection = DriverManager.getConnection(dbhost, user, password);

                PreparedStatement pst = connection.prepareStatement(delRow);

                pst.executeUpdate();

                tblModel.removeRow(jTable1.getSelectedRow());
            } else {
                if (jTable1.getRowCount() == 0) {
                    JOptionPane.showMessageDialog(null, "Table is empyty. Cannot delete from an empty table");
                } else {
                    JOptionPane.showMessageDialog(null, "Please select a single row for delete");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

