    public ArrayList<Inventory> getInventoryList() {
        ArrayList<Inventory> inventoryList = new ArrayList<>();
        final String dbhost = "jdbc:postgresql://localhost:5432/InventoryTrackerDB";
        final String user = "postgres";
        final String password = "password";

        try {
            Connection connection = DriverManager.getConnection(dbhost, user, password);
            String sql = "SELECT * FROM INVENTORY";
            PreparedStatement pst = connection.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            Inventory inventory;

            while (rs.next()) {
                inventory = new Inventory(rs.getInt("inventory_id"), rs.getString("name"), rs.getString("descr"), rs.getInt("quantity"), rs.getInt("condition_id"), rs.getInt("location_id"));
                inventoryList.add(inventory);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
        }

        return inventoryList;
    }

    public void showInventoryData() {
        ArrayList<Inventory> list = getInventoryList();

        for (int i = 0; i < list.size(); i++) {
            System.out.println(list.get(i) + "this is list");
        }

        DefaultTableModel tm = (DefaultTableModel) jTable1.getModel();
        Object[] row = new Object[6];

        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getInventoryId();
            row[1] = list.get(i).getName();
            row[2] = list.get(i).getDescr();
            row[3] = list.get(i).getQuantity();
            row[4] = list.get(i).getConditionId();
            row[5] = list.get(i).getLocationId();
            tm.addRow(row);
        }
    }

